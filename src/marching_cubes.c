int MC_index(const Vec3 corners[8], SDF f, float isolevel)
{
    int mc_index = 0;
    if (f(corners[0]) < isolevel) mc_index |= 1;
    if (f(corners[1]) < isolevel) mc_index |= 2;
    if (f(corners[2]) < isolevel) mc_index |= 4;
    if (f(corners[3]) < isolevel) mc_index |= 8;
    if (f(corners[4]) < isolevel) mc_index |= 16;
    if (f(corners[5]) < isolevel) mc_index |= 32;
    if (f(corners[6]) < isolevel) mc_index |= 64;
    if (f(corners[7]) < isolevel) mc_index |= 128;
    return mc_index;
}

static void MC_interpolateVertexPosition(const Vec3 a, const Vec3 b, SDF f,
        float isolevel, Vec3 dst)
{
    float fa = f(a);
    float fb = f(b);
    if (fabsf(isolevel - fa) < 0.0001f || fabsf(fa - fb) < 0.0001f)
    {
        dst[0] = a[0];
        dst[1] = a[1];
        dst[2] = a[2];
        return;
    }
    if (fabsf(isolevel - fb) < 0.0001f)
    {
        dst[0] = b[0];
        dst[1] = b[1];
        dst[2] = b[2];
        return;
    }
    float t = (isolevel - fa) / (fb - fa);
    dst[0] = a[0] + t * (b[0] - a[0]);
    dst[1] = a[1] + t * (b[1] - a[1]);
    dst[2] = a[2] + t * (b[2] - a[2]);
    return;
}

// Inigo Quilez trapezoidal algorithm                                       
// These normals could be computed in vertex shader if it had access to the SDF
static void MC_normal(const Vec3 p, SDF f, Vec3 dst)
{
    const float h = 0.01f;

    Vec3 x1 = {  h, -h, -h };
    Vec3 x2 = { -h, -h,  h };
    Vec3 x3 = { -h,  h, -h };
    Vec3 x4 = {  h,  h,  h };

    Vec3 x5, x6, x7, x8;
    Vec3_add(p, x1, x5);
    Vec3_add(p, x2, x6);
    Vec3_add(p, x3, x7);
    Vec3_add(p, x4, x8);

    Vec3_scale(x1, f(x5), x1);
    Vec3_scale(x2, f(x6), x2);
    Vec3_scale(x3, f(x7), x3);
    Vec3_scale(x4, f(x8), x4);

    Vec3_add(x1, x2, x1);
    Vec3_add(x3, x4, x3);
    Vec3_add(x1, x3, x1);

    Vec3_normalize(x1, dst);
}

int MC_vertices(const Vec3 corners[8], SDF f, float isolevel, int mc_index,
        Vertex vertices[3])
{
    if (EDGE_TABLE[mc_index] & 1)
    {
        MC_interpolateVertexPosition(
                corners[0],
                corners[1],
                f,
                isolevel,
                vertices[0].position
        );
        //MC_normal(vertices[0].position, f, vertices[0].normal);
    }
    if (EDGE_TABLE[mc_index] & 8)
    {
        MC_interpolateVertexPosition(
                corners[3],
                corners[0],
                f,
                isolevel,
                vertices[1].position
        );
        //MC_normal(vertices[1].position, f, vertices[1].normal);
    }
    if (EDGE_TABLE[mc_index] & 256)
    {
        MC_interpolateVertexPosition(
                corners[0],
                corners[4],
                f,
                isolevel,
                vertices[2].position
        );
        //MC_normal(vertices[2].position, f, vertices[2].normal);
    }
    return 3;
}

int MC_indices(int mc_index, unsigned int vertex_offset,
        const unsigned int edge_offsets[12], unsigned int indices[15])
{
    int index_count = 0;
    while (TRI_TABLE[mc_index][index_count] != -1)
    {
        int edge_id = TRI_TABLE[mc_index][index_count];
        indices[index_count] = vertex_offset + edge_offsets[edge_id];
        index_count += 1;
    }
    return index_count;
}
